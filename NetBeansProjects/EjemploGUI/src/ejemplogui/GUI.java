    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
     
        NuevoInmueble diag = new NuevoInmueble(this, true);
        diag.setVisible(true);
        if(diag.inm != null){
            modelo.addRow(new Object[]{diag.inm.getTipo(), diag.inm.getMetros(), diag.inm.getDireccion()});      
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

    private void modificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modificarActionPerformed
       
       Inmueble inm = new Inmueble();
       inm.setTipo((String)tabla.getValueAt(tabla.getSelectedRow(), 0));
       inm.setMetros((Integer)tabla.getValueAt(tabla.getSelectedRow(), 1));
       inm.setDireccion((String)tabla.getValueAt(tabla.getSelectedRow(), 2));
        
       NuevoInmueble diag = new NuevoInmueble(this, true, inm);
       diag.setVisible(true);
       if(diag.inm != null){
           tabla.setValueAt(diag.inm.getTipo(), tabla.getSelectedRow(), 0);
           tabla.setValueAt(diag.inm.getMetros(), tabla.getSelectedRow(), 1);
           tabla.setValueAt(diag.inm.getDireccion(), tabla.getSelectedRow(), 2);
       }
       
        
    }//GEN-LAST:event_modificarActionPerformed

    private void tablaMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaMousePressed

        if(tabla.getSelectedRow() != -1){
            modificar.setEnabled(true);
         //   jButton2.setEnabled(true);
        }else{
            modificar.setEnabled(false);
          //  jButton2.setEnabled(false);
        }
    }//GEN-LAST:event_tablaMousePressed

