    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
   
        if(!metros.getText().equals("") &&
           !direccion.getText().equals("") &&
          (isCasa.isSelected() | isPiso.isSelected())){
                
            inm = new Inmueble();
            inm.setMetros(Integer.valueOf(metros.getText()));
            inm.setDireccion(direccion.getText());
            
            if(isCasa.isSelected()){
                inm.setTipo("Casa");
            }else{
                inm.setTipo("Piso");
            }    
        }
        
        dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

