    private void botonAceptarCocheActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAceptarCocheActionPerformed
        if(!marcaCoche.getText().equals("") &&
               !modeloCoche.getText().equals("") &&
               !precioCoche.getText().equals("") &&
               !kilometraje.getText().equals("") &&
              (isSegundaMano.isSelected() | isNotSegundaMano.isSelected())){
                
            Coche coche = new Coche();
            coche.setMarca(marcaCoche.getText());
            coche.setModelo(modeloCoche.getText());
            coche.setPrecio(Float.valueOf(precioCoche.getText()));
            coche.setKilometraje(Integer.valueOf(kilometraje.getText()));
            if(isSegundaMano.isSelected()){
                coche.setEsSegunda(true);
            }else{
                coche.setEsSegunda(false);
            }
            coche.setId(++contadorId);
          v = coche;
        }
        dispose();
    }//GEN-LAST:event_botonAceptarCocheActionPerformed

    private void botonAceptarCamionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAceptarCamionActionPerformed
        if(!marcaCamion.getText().equals("") &&
            !modeloCamion.getText().equals("") &&
            !precioCamion.getText().equals("") &&
            !carga.getText().equals("")){

            Camion camion = new Camion();
            camion.setMarca(marcaCamion.getText());
            camion.setModelo(modeloCamion.getText());
            camion.setPrecio(Float.valueOf(precioCamion.getText()));
            camion.setCarga(Integer.valueOf(carga.getText()));
            camion.setId(++contadorId);
           v = camion;
        }
        
        dispose();
    }//GEN-LAST:event_botonAceptarCamionActionPerformed

